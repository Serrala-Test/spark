package org.apache.spark.mllib.tree

import org.apache.spark.mllib.util.MLlibTestSparkContext
import org.scalatest.FunSuite

import org.apache.spark.mllib.tree.loss._

/**
 * Test suite for [[Losses]]
 */
class LossesSuite extends FunSuite with MLlibTestSparkContext{
  test("Losses generated by fromString method") {
    try {
      Losses.fromString("SquaredError")
      Losses.fromString("AbsoluteError")
      Losses.fromString("LogLoss")
    } catch {
      case _: IllegalArgumentException => fail("Loss function is not supported")
    }
  }

}
