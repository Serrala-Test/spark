--test case for bitnot function
SELECT ~1, ~1000, ~1000000, ~123456789123;
SELECT BITNOT(1), BITNOT(1000), BITNOT(1000000), BITNOT(123456789123);

--test case for bitand function
SELECT 1&2, 1000&2, 1000000&4, 123456789123&5;
SELECT BITAND(1, 2), BITAND(1000, 2), BITAND(1000000, 4), BITAND(123456789123, 5);

--test case for bitor function
SELECT 1|2, 1000|2, 1000000|4, 123456789123|5;
SELECT BITOR(1, 2), BITOR(1000, 2), BITOR(1000000, 4), BITOR(123456789123, 5);

--test case for bitxor function
SELECT 1^2, 1000^2, 1000000^4, 123456789123^5;
SELECT BITXOR(1, 2), BITXOR(1000, 2), BITXOR(1000000, 4), BITXOR(123456789123, 5);
